                          schemeMap = { light: 'black', dark: 'white' },
                          fileName = `icon32.png?v=${app.latestResourceCommitHash}`
                    if (preload)
                        Object.keys(schemeMap).forEach(scheme =>
                            new Image().src = `${baseURL}/${schemeMap[scheme]}/${fileName}`)
